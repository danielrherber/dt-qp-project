function I = BrysonDenham_F(l)

    %----------------------------------------------------------------------
    if (l > 0) && (l <= 1/6)
        I = 4/(9*l);
    %----------------------------------------------------------------------
    elseif (l >= 1/6) && (l <= 1/4)
        I = 2 + 6*(1-4*l)^2;
    %----------------------------------------------------------------------
    elseif (l >= 1/4)
        I = 2;
    %----------------------------------------------------------------------
    else
        I = NaN;
    end